def foo(n):
    """!!!

    Параметры:
        - n (int): число.

    Сложность: !!!.
    """
    res = []
    for i in range(1, n + 1): #O(N)
        divisors = 0
        j = 2
        while j < i and divisors == 0: # O(1)
            if i % j == 0: # O(1)
                divisors += 1
            j += 1

        if divisors == 0: # O(1)
            res.append(i)

    return res

