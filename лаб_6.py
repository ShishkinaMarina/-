def foo(a):
    """!!!

    Сложность: !!!.
    """
    for i in range(len(a), 0, -1): # O(len(a))
        for j in range(1, i): # O(N)
            if a[j-1] > a[j]: # O(1)
                a[j-1], a[j] = a[j], a[j-1] # O(1)
    return a


