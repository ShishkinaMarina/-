def foo(i):
    """!!!

    Параметры:
        - i (int): число.

    Сложность: !!!.
    """
    digits = "0123456789"
    if i == 0: # O(1)
        return "0"
    result = ""
    while i > 0: # O(N)
        result = digits[i%10] + result
        i = i // 10
    return result

